def replace_paragraph_text(paragraph, text: str) -> None:
    for run in list(paragraph.runs):
        paragraph._p.remove(run._r)
    run = paragraph.add_run(text)
    style_name = paragraph.style.name if paragraph.style else "Normal"
    if style_name == "Title":
        set_run_font(run, 16, bold=True, east_asia="黑体")
    elif style_name.startswith("Heading"):
        size = 14 if style_name == "Heading 1" else 12 if style_name == "Heading 2" else 10.5
        set_run_font(run, size, bold=True, east_asia="黑体")
    elif style_name in {"FigureCaption", "TableCaption"}:
        set_run_font(run, 8.5)
    elif style_name == "TableNote":
        set_run_font(run, 8.0)
    else:
        set_run_font(run, 10.5)


